import { config } from "@/lib/config";

const configuredSbtAddress = process.env.NEXT_PUBLIC_SBT_ADDRESS?.trim();

if (configuredSbtAddress && !/^0x[a-fA-F0-9]{40}$/.test(configuredSbtAddress)) {
  throw new Error(
    "NEXT_PUBLIC_SBT_ADDRESS must be a valid 20-byte EVM address.",
  );
}

export const SBT_CONTRACT = configuredSbtAddress
  ? (configuredSbtAddress as `0x${string}`)
  : undefined;

export const isSbtConfigured = Boolean(SBT_CONTRACT);

export function requireSbtContract() {
  if (!SBT_CONTRACT) {
    throw new Error(
      "NEXT_PUBLIC_SBT_ADDRESS is required. Deploy RitualSBT and set it in .env.local.",
    );
  }

  return SBT_CONTRACT;
}

export const getSbtExplorerUrl = (txHash: string) => {
  const baseUrl = config.explorerUrl.replace(/\/+$/, "");
  return `${baseUrl}/tx/${txHash}`;
};

export const getIpfsGatewayUrl = (uri: string) => {
  return getIpfsGatewayUrls(uri)[0];
};

export const getIpfsGatewayUrls = (uri: string) => {
  if (!uri.startsWith("ipfs://")) {
    return [uri];
  }

  const ipfsPath = uri.slice("ipfs://".length).replace(/^ipfs\//, "");

  return [
    `https://gateway.pinata.cloud/ipfs/${ipfsPath}`,
    `https://ipfs.io/ipfs/${ipfsPath}`,
    `https://cloudflare-ipfs.com/ipfs/${ipfsPath}`,
  ];
};

